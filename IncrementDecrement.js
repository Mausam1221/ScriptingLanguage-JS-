
// wap that uses increment(++) and decrement (--) operator to manipulate variables and display the results

let num = 5;
console.log("Original Value:", num);
num++;
console.log("After Increment:", num);
num--;
console.log("After Decrement:", num);
num += 4;
console.log("After Increment and Assign:", num);

num -= 3;
console.log("After Decrement and Assign:", num);
