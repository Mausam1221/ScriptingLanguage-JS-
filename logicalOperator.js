// build a program that uses logical operator(AND,OR,NOT) to check multiple condition and prints the result
let firstNumber=100;
let secondNumber=10;
if(firstNumber>10 && secondNumber<200){
    console.log("first number is greater than 100 and smaller than 200");
}
if(firstNumber>10 || secondNumber<200){
    console.log("first number is greater than 100 or smaller than 200");
}
if(firstNumber!=secondNumber){
    console.log("first number is not equal to second number");
}
